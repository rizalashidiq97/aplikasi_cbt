<b>SEBELUM  UJIAN DIMULAI</b><br>
1.  Sudah mengetahui tempat, ruang, dan waktu ujian.<br>
2.  Membawa:<br>
<ol type="a"><li>  Tanda peserta ujian</li> 
<li>  Identitas diri yang ada foto diri (KTP/SIM/Paspor, Ijazah/SKL/Transkrip Nilai)</li></ol>
3.  Sudah sampai di lokasi ujian paling lambat 30 menit sebelum ujian dimulai dan menunggu sampai dipersilakan masuk oleh pengawas ujian.<br>
4.  Menempati tempat duduk sesuai dengan nomor peserta ujian setelah dipersilakan masuk ruang ujian oleh pengawas ujian.<br>
5.  Meletakkan dokumen-dokumen pada butir 3 di atas meja/kursi ujian.<br>
6.  Tidak diperbolehkan membawa ke kursi / tempat ujian :
<ol type="a"><li>  Segala macam buku, catatan, dan kertas apapun;</li>
<li>  Segala macam alat hitung, antara lain kalkulator, mistar hitung,sempoa, dan lain sebagainya;</li>
<li>  Segala macam alat elektronik antara lain, radio komunikasi, handytalkie, telepon seluler, alat bantu pendengaran, jam tangan digital, dan lain sebagainya.</li></ol><br>

<b>SELAMA UJIAN</b><br>

1.  Peserta diperbolehkan mengerjakan setelah diberi perintah oleh pengawas ujian.<br>
2.  Peserta boleh melaporkan kepada pengawas jika terjadi masalah pada computer CAT.<br>
3.  Peserta tidak diperbolehkan meninggalkan ruang ujian sampai dengan ujian dinyatakan selesai oleh pengawas ujian.<br>
4.  Peserta dapat menggunakan bagian halaman yang kosong pada kertas yang disediakan untuk menyelesaikan soal-soal hitungan.<br>
5.  Peserta harus mengerjakan semua soal pada Komputer CAT.<br>
6.  Peserta yang sudah menyelesaikan seluruh soal ujian sebelum waktu ujian habis tetap duduk tenang di tempat   dan gunakan sisa waktu untuk memeriksa kembali jawabannya.<br><br>

<b>SETELAH SELESAI</b><br>

1.  Peserta harus segera berhenti mengerjakan soal ujian setelah tanda waktu ujian habis.<br>
2.  Peserta tetap  duduk di  tempat sampai waktu selesai.<br>
